pn = int(input('digite o primeiro número: '))
sn = int(input('digite o segunto número: '))
tn = int(input('digite o terceiro número: '))
lista = [pn, sn, tn]
print('o maior número digitado é: {}'.format(max(lista)))
print('o menor número digitado é: {}'.format(min(lista)))

#use max para saber o maior número (máximo) e min para minimo = menor